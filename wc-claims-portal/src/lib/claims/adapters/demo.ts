export class DemoClaimsAdapter implements ClaimsAdapter {
  async listClaims(): Promise<ClaimSummary[]> {
    return CLAIMS.map(toSummary);
  }

  async getClaim(id: string): Promise<Claim | null> {
    return CLAIMS.find((c) => c.id === id) ?? null;
  }

  async getDashboardMetrics(): Promise<DashboardMetrics> {
    const now = new Date();
    const monthStart = new Date(now.getFullYear(), now.getMonth(), 1).toISOString();
    return {
      openClaims: CLAIMS.filter((c) => c.status !== "closed").length,
      pendingReview: CLAIMS.filter((c) => c.status === "pending" || c.status === "escalated").length,
      closedThisMonth: CLAIMS.filter((c) => c.status === "closed" && c.updatedAt >= monthStart).length,
      highRisk: CLAIMS.filter((c) => c.status === "escalated").length,
    };
  }

  async getRecentActivity(limit = 7): Promise<ActivityItem[]> {
    const now = Date.now();
    return ACTIVITY_EVENTS.slice(0, limit).map(({ claimId, event, minutesAgo }) => {
      const claim = CLAIMS.find((c) => c.id === claimId)!;
      return {
        claimId,
        claimantName: claim.claimant.name,
        event,
        status: claim.status,
        timestamp: new Date(now - minutesAgo * 60 * 1000).toISOString(),
      };
    });
  }
}
