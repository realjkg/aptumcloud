function toSummary(c: Claim): ClaimSummary {
  return {
    id: c.id,
    claimantName: c.claimant.name,
    dateOfLoss: c.dateOfLoss,
    employer: c.employer.name,
    jurisdiction: c.jurisdiction,
    type: c.type,
    status: c.status,
    adjuster: c.adjuster,
    updatedAt: c.updatedAt,
  };
}

